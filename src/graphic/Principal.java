    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String text = jTextField1.getText();
            if (text.equals("")) {
                JOptionPane.showMessageDialog(this, "Debe introducir un número de registro a recuperar", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Long idRegistro;
            try {
                idRegistro = Long.parseLong(text);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Debe introducir un número de registro a recuperar válido", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            operaciones.recuperarDocumento(this, Metodos.getKeyStore(), Metodos.getKeyStorePass(), Metodos.getClientCN(), Metodos.getTipoClave(), idRegistro);
            try {
                Desktop.getDesktop().open(new File("Recibidos"));
            } catch (Exception ex) {

            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Excepción inesperada", "Error", JOptionPane.ERROR_MESSAGE);
        }
        System.exit(0);
    }//GEN-LAST:event_jButton3ActionPerformed

