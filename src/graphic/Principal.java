    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDragEnabled(true);
        if (jTextField3.getText().contains("/")) {
            chooser.setCurrentDirectory(new File(jTextField3.getText().substring(0, jTextField3.getText().lastIndexOf("/"))));
        }
        int op = chooser.showOpenDialog(this);
        if (op == JFileChooser.APPROVE_OPTION) {
            jTextField3.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String nombreDoc = jTextField2.getText();
        String docPath = jTextField3.getText();
        if (nombreDoc.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe introducir un nombre de documento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (docPath.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un documento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!new File(docPath).exists() || new File(docPath).isDirectory()) {
            JOptionPane.showMessageDialog(this, "El documento seleccionado no existe o no es un documento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean confidencialidad = jRadioButton1.isSelected();
        try {
            operaciones.registrarDocumento(this, Metodos.getKeyStore(), Metodos.getTipoClave(), Metodos.getClientCN(), Metodos.getKeyStorePass(), nombreDoc, docPath, confidencialidad);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Excepción inesperada", "Error", JOptionPane.ERROR_MESSAGE);
        }
        System.exit(0);

    }//GEN-LAST:event_jButton2ActionPerformed

